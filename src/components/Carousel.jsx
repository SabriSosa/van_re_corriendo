// Because this is an inframe, so the SSR mode doesn't not do well here.
// It will work on real devices.
function SimpleCarousel() {
  const [nextSlide, setNextSlide] = useState(-1);

  return (
    <Container fluid>
      <TitleComp title1="Nuestras" title2="Postales" />
      <Carousel
        ssr
        showDots={true}
        responsive={responsive}
        focusOnSelect={true}
        itemClass="image-item"
        beforeChange={(nextSlide) => {
          console.log("next 2", nextSlide);
          setNextSlide(nextSlide);
        }}
        autoPlay={false}
        // autoPlaySpeed={2000}
        infinite={true}
        pauseOnHover
        slidesToSlide={1}
      >
        {images.slice(0, 20).map((image, index) => {
          return (
            <Container
              id={index}
              key={index}
              className={`image-carousel ${
                index === nextSlide ? "is-selected" : ""
              }`}
              style={{ backgroundImage: `url(${image})` }}
            >
              <div className="text-info-carousel">
                7 cosas alucinantes que hacer en Zapala {index}
              </div>
            </Container>
          );
        })}
      </Carousel>
    </Container>
  );
}

export default SimpleCarousel;
